// Operadores lÃ³gicos
let a = true;
let b = false;

console.log(a);
console.log(b);

// Operador AND
// devuelve verdadero en caso de que ambos valores sean verdaderos.
console.log(`${a} && ${b} -> ${a && b}`)

// Operador 0R
// devuelve verdadero en caso de que cualquier valor sea verdadero.
console.log(`${a} || ${b} -> ${a || b }`)

// Operador NOT
// invierte el valor original, es decir true -> false
console.log(`${a} -> ${!a}`)