// Operadores de asignación
let miNumero = 10;
console.log(miNumero);

miNumero = 33;
console.log(miNumero);

// Operador de +=
miNumero += 5;
console.log(miNumero); // se suma el añadido al valor de la variable.

// Operador de -=
miNumero -= 3;
console.log(miNumero);

// Operador de +=
miNumero *= 2;
console.log(miNumero);

// Operador de /=
miNumero /= 2;
console.log(miNumero);

// Operador de %=
miNumero %= 2;
console.log(miNumero);

// Operador de **=
miNumero **= 2;
console.log(miNumero);