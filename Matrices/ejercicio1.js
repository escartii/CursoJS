// Una matriz es un array de 2 dimensiones.
// Declaracion de una matriz
let matriz = [[],[]];
// Modificar los valores
matriz[0][0] = 100;
matriz[0][1] = 200;
matriz[0][2] = 300;
matriz [1][0] = 101;
matriz [1][1] = 102;
matriz [1][2] = 103;

// Leer los valores
console.log(`Elemento[0][1] - ${matriz[0][1]}`)
console.log(`Elemento[1][0] - ${matriz[1][0]}`)
